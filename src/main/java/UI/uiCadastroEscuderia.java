    private void cadastrarEscuderiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarEscuderiaActionPerformed
        int id1, id2, combustivel1, combustivel2;
        
        id1 = Integer.parseInt(idCarro1.getText());
        id2 = Integer.parseInt(idCarro2.getText());
        
        combustivel1 = Integer.parseInt(combustivelCarro1.getText());
        combustivel2 = Integer.parseInt(combustivelCarro2.getText());
        
        Escuderia novaEscuderia = new Escuderia(nomeEscuderia.getText());
        novaEscuderia.adicionarCarros(new Carro (id1, nomeEscuderia.getText(), combustivel1));
        novaEscuderia.adicionarCarros(new Carro (id2, nomeEscuderia.getText(), combustivel2));
        novaEscuderia.adicionarPilotos(new Piloto(nomePiloto1.getText()));
        novaEscuderia.adicionarPilotos(new Piloto(nomePiloto2.getText()));
        novaEscuderia.adicionarEngenheiros(new Engenheiro(nomeEngenheiro1.getText()));
        novaEscuderia.adicionarEngenheiros(new Engenheiro(nomeEngenheiro2.getText()));
        novaEscuderia.adicionarMecanicos(new Mecanico(nomeMecanico1.getText()));
        novaEscuderia.adicionarMecanicos(new Mecanico(nomeMecanico2.getText()));
        novaEscuderia.adicionarMecanicos(new Mecanico(nomeMecanico3.getText()));
        novaEscuderia.adicionarMecanicos(new Mecanico(nomeMecanico4.getText()));
        
        uiPrincipal.sistema.adicionarEscuderia(novaEscuderia);
        
        msgFinal.setText("Escuderia Cadastrada!!");
        msgFinal.setVisible(true);
    }//GEN-LAST:event_cadastrarEscuderiaActionPerformed

